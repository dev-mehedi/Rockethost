// first one
function incrementValue()
{
    var value = parseInt(document.getElementById('number1').value, 10);
    value = isNaN(value) ? 0 : value;
    if(value<50){
        value++;
            document.getElementById('number1').value = value;
    }
}
function decrementValue()
{
    var value = parseInt(document.getElementById('number1').value, 10);
    value = isNaN(value) ? 0 : value;
    if(value>1){
        value--;
            document.getElementById('number1').value = value;
    }

}
